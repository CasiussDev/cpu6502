#[cfg(feature = "gen_write_cycle_query")]
mod inner {
    use cpu6502::instr::ClockEndStatus;
    use cpu6502::instr::{Instruction, InstructionSequenceMode};
    use cpu6502::MemorySpace;
    use strum::IntoEnumIterator;

    const COMMENT_HEADER: &str = "// THIS FILE HAS BEEN GENERATED BY gen_write_cycle_query";

    #[derive(Default)]
    struct MemoryWriteDetector {
        write_access: bool,
    }

    impl MemorySpace for MemoryWriteDetector {
        fn read(&mut self, _addr: u16) -> u8 {
            0
        }

        fn write(&mut self, _data: u8, _addr: u16) {
            self.write_access = true;
        }
    }

    pub fn main() {
        use std::fs;
        use std::io::Write;

        let mut switch_options = String::new();

        for instr in Instruction::iter() {
            let mut step = 0;
            loop {
                let mut memory = MemoryWriteDetector::default();
                let mut regs = cpu6502::RegisterFile::default();

                let result = cpu6502::execute(instr, step, &mut regs, &mut memory);

                let instr: InstructionSequenceMode = instr.into();
                switch_options.push_str(&format!(
                    "({instr:?}, {step}) => {},\n",
                    memory.write_access
                ));

                step += 1;

                if result != ClockEndStatus::Continue {
                    break;
                }
            }
        }

        fs::create_dir_all("src/cpu").expect("Could not create directory");

        let mut code_file =
            fs::File::create("src/cpu/write_cycle_query.rs").expect("Could not create file");
        write!(
            code_file,
            include_str!("write_cycle_query.rs.txt"),
            COMMENT_HEADER, switch_options
        )
            .expect("Could not write file");

        std::process::Command::new("rustfmt")
            .arg("src/cpu/write_cycle_query.rs")
            .output()
            .expect("Failed to execute rustfmt");
    }
}

#[cfg(feature = "gen_write_cycle_query")]
pub use inner::main;

#[cfg(not(feature = "gen_write_cycle_query"))]
fn main() {}

