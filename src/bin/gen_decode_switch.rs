#[cfg(feature = "disassembly")]
use cpu6502::decode;
#[cfg(feature = "disassembly")]
use cpu6502::instr::destruct_instruction;

#[cfg(feature = "disassembly")]
const COMMENT_HEADER: &str = "// THIS FILE HAS BEEN GENERATED BY gen_decode_switch";

#[cfg(not(feature = "disassembly"))]
fn main() {}

#[cfg(feature = "disassembly")]
fn main() {
    use std::fs;
    use std::io::Write;

    let mut switch_options = String::new();

    for opcode in 0..=u8::MAX {
        let decoded = decode(opcode);

        switch_options.push_str(&format!("{opcode:#04X} => {decoded:?}\n,\n"));
    }

    fs::create_dir_all("src/instr/opcodes").expect("Could not create directory");

    let mut code_file =
        fs::File::create("src/instr/opcodes/decode_switch.rs").expect("Could not create file");
    write!(
        code_file,
        include_str!("decode_switch.rs.txt"),
        COMMENT_HEADER, switch_options
    )
    .expect("Could not write file");

    std::process::Command::new("rustfmt")
        .arg("src/instr/opcodes/decode_switch.rs")
        .output()
        .expect("Failed to execute rustfmt");
}
